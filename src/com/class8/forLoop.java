package com.class8;

public class forLoop {
//I want to say good morning 3 times
	public static void main(String[] args) {
	int num=1;
	while (num<+5) {
		System.out.println("Good Morning");
		num++;
	}
	//I want to say good morning 5 times
	for (int i=0; i<=4; i++){
		System.out.println("Good Morning2");
	}
	}
}
